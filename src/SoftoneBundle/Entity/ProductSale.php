     * @var integer
     */
    private $id;

    /**
     * Set title
     *
     * @param string $title
     *
     * @return ProductSale
     */
    public function setTitle($title) {
        $this->title = $title;

        return $this;
    }

    /**
     * Get title
     *
     * @return string
     */
    public function getTitle() {
        return $this->title;
    }

    /**
     * Set expired
     *
     * @return string
     *
     * @return ProductSale
     */
    public function setExpired($expired) {
        $this->expired = $expired;

        return $this;
